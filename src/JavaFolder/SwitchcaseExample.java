package JavaFolder;

public class SwitchcaseExample {

	public static void main(String[] args) {
		 int age=11;
		 
		 switch(age) {
		 case 10 : System.out.println("he is child");
		 break;
		 case 18 : System.out.println("he is teen");
		 break;
		 case 60 : System.out.println("he is Sr citizen");
		 break;
		 case 80 :  System.out.println("he is Old");
		 default :  System.out.println("Sorry no age is matching");
		 }

	}

}
