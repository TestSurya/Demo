package JavaFolder;

public class declarationofVariables {

	public static void main(String[] args) {
	
		int a;//declaration
		int b;//declaration
		int c;//declaration
		
		a=10;//Assigning of values
		b=30;//Assigning of values
		c=a+b;//Assigning of values
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		
		int x,y,z;//declaration
		x=9;y=6;z=x+y;//Assigning of values
		System.out.println(x);
		System.out.println(y);
		System.out.println(z);
	
		int p=9;//Declaration and assigning (Literal declaration)
		int m=7;//Declaration and assigning (Literal declaration)
		System.out.println(p);
		System.out.println(m);
		
	}

}
